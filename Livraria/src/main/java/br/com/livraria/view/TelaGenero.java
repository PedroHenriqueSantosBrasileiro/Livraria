    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
         iniciaGeneroDao();
         
         Genero genero = new Genero(txtNome.getText());
         
         generoDao.adicionarGenero(genero);
         
         JOptionPane.showMessageDialog(null, "Genero Adicionado com sucesso");
         
         prencherTabela();
         em.close();
         limpaCampo();
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void tblGeneroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblGeneroMouseClicked
        if(tblGenero.getSelectedRow() != -1){
            String nome = (String) tblGenero.getValueAt(tblGenero.getSelectedRow(), 1);
            
            txtNome.setText(nome);
        }
    }//GEN-LAST:event_tblGeneroMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(tblGenero.getSelectedRow() != -1){
            
            int id = (int) tblGenero.getValueAt(tblGenero.getSelectedRow(), 0);
            
            iniciaGeneroDao();
            
            generoDao.excluirGenero(id);
            em.close();
            limpaCampo();
            prencherTabela();
            
            JOptionPane.showMessageDialog(null, "Genero excluido com sucesso");
            
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        
        if(tblGenero.getSelectedRow() != -1){
            
            iniciaGeneroDao();
            
            int id = Integer.parseInt(tblGenero.getValueAt(tblGenero.getSelectedRow(), 0).toString());
            
            String nome = txtNome.getText();
            
            iniciaGeneroDao();
            
            generoDao.editarGenero(id, nome);
            
            limpaCampo();
            prencherTabela();
            em.close();
            
            JOptionPane.showMessageDialog(null, "Genero editado com sucesso");
            
        }
        
    }//GEN-LAST:event_btnEditarActionPerformed


