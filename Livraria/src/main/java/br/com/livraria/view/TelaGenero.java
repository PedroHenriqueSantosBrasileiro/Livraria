    private void prencherTabela(){
        iniciaGeneroDao();
        
        ArrayList<Genero> generos = (ArrayList<Genero>) generoDao.listarGeneros();
        
        DefaultTableModel modelo = (DefaultTableModel) tblGenero.getModel();
        
        modelo.setNumRows(0);
        
        for(Genero genero: generos){
            modelo.addRow(new Object[]{genero.getId(),genero.getNome()});
        }
        em.close();
    }

